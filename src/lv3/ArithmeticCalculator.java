package lv3;

public class ArithmeticCalculator<T> {
    public int add(int x, int y) {
        return x + y;
    }

    public int subtract(int x, int y) {
        return x - y;
    }

    public int multiply(int x, int y) {
        return x * y;
    }

    public int division(int x, int y) {
        return x / y;
    }
}
